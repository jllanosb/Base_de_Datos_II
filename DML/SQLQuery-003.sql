DECLARE @o INT = 1;
WHILE @o <= 50 -- Limitar para que no todas las reservas tengan transporte
BEGIN
    DECLARE @id_reserva_rt INT = (SELECT TOP 1 id_reserva FROM reservas ORDER BY NEWID());
    DECLARE @id_vehiculo_rt INT = (SELECT TOP 1 id_vehiculo FROM vehiculos ORDER BY NEWID());
    DECLARE @fecha_servicio_rt DATE = (SELECT fecha_inicio FROM reservas WHERE id_reserva = @id_reserva_rt);
    DECLARE @hora_inicio_rt TIME = '08:00:00';
    DECLARE @hora_fin_rt TIME = '18:00:00';
    DECLARE @precio_rt DECIMAL(10,2) = 50.00 + (RAND() * 150.00);

    IF @id_vehiculo_rt IS NOT NULL AND NOT EXISTS (SELECT 1 FROM reserva_transporte WHERE id_reserva = @id_reserva_rt AND id_vehiculo = @id_vehiculo_rt AND fecha_servicio = @fecha_servicio_rt)
    BEGIN
        INSERT INTO reserva_transporte (id_reserva, id_vehiculo, fecha_servicio, hora_inicio, hora_fin, punto_recojo, punto_destino, precio) VALUES
        (@id_reserva_rt, @id_vehiculo_rt, @fecha_servicio_rt, @hora_inicio_rt, @hora_fin_rt, CONCAT('Hotel Cliente ', @o), CONCAT('Lugar Turístico ', @o), @precio_rt);
    END;
    SET @o = @o + 1;
END;
